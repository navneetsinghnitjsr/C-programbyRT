//write a program to display the size of every data type.
#include<iostream>
using namespace std;
int main()
{
	cout<<"The size of short Integer is :"<<sizeof(short int);
	cout<<"\n1.The size of unsigned Integer is :"<<sizeof(unsigned int);
	cout<<"\n2.The size of signed Integer is :"<<sizeof(signed int);
	cout<<"\n3.The size of long Integer is :"<<sizeof(long int);
	cout<<"\n4.The size of character is :"<<sizeof(char);
	cout<<"\n\n5.The size of unsigned character is :"<<sizeof(unsigned char);
	cout<<"\n\n6.The size of Signed is :"<<sizeof(signed char);
	cout<<"\n\n7.The size of Floating point Number is :"<<sizeof(float);
	cout<<"\n\n\n8.The size of Double number is :"<<sizeof(double);
	return 0;
	
} 
